function slug(text) {
  return text.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function loadRecipes() {
  const saved = localStorage.getItem("chef_deluxe_custom_recipes");
  const custom = saved ? JSON.parse(saved) : [];
  return [...starterRecipes, ...custom];
}

