function renderIdeas(ideas, ingredients, style) {
  if (!ideasList) return;
  ideasList.innerHTML = ideas.map((idea, index) => `
    <article class="idea-card">
      <h3>${idea.title}</h3>
      <p>${idea.reason}</p>
      <div class="idea-tags">
        <span class="idea-chip">${idea.category}</span>
        <span class="idea-chip">${idea.method}</span>
        <span class="idea-chip">${idea.heat}</span>
      </div>
      <button class="primary-btn" onclick="createGeneratedRecipe(${index})">Build this recipe</button>
    </article>
  `).join("");
  window.__chefIdeaState = { ideas, ingredients, style };
}

