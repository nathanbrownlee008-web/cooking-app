function createGeneratedRecipe(index) {
  const stateData = window.__chefIdeaState;
  if (!stateData) return;
  const chosenIdea = stateData.ideas[index];
  const ingredients = stateData.ingredients;
  const smart = buildSmartRecipe(chosenIdea, ingredients);
  const difficulty = (document.getElementById("gDifficulty") || { value: "" }).value.trim() || "Custom";
  const time = (document.getElementById("gTime") || { value: "" }).value.trim() || "30 min";
  const serves = (document.getElementById("gServes") || { value: "" }).value.trim() || "2 people";
  const recipe = {
    id: slug(smart.title + "-" + Date.now()),
    title: smart.title,
    category: smart.category,
    difficulty, time, serves,
    description: smart.description,
    tags: smart.tags,
    ingredients,
    seasoning: smart.seasoning,
    notes: smart.notes,
    steps: smart.steps,
    plating: smart.plating
  };
  saveCustomRecipe(recipe);
  recipes = loadRecipes();
  renderFilters();
  renderRecipes();
  closeIdeasModal();
  closeGeneratorModal();
  ["gIngredients","gStyle","gCategory","gDifficulty","gTime","gServes"].forEach(id => {
    const el = document.getElementById(id);
    if (el) el.value = "";
  });
  openRecipe(recipe.id);
}

function buildGeneratedRecipe() {
  const ingredients = getLines("gIngredients");
  const style = (document.getElementById("gStyle") || { value: "" }).value.trim() || "";
  const providedCategory = (document.getElementById("gCategory") || { value: "" }).value.trim();
  if (!ingredients.length) {
    alert("Add at least one ingredient first.");
    return;
  }
  const ideas = buildDishIdeas(ingredients, style, providedCategory);
  renderIdeas(ideas, ingredients, style);
  openIdeasModal();
}

function saveNewRecipe() {
  const title = (document.getElementById("fTitle") || { value: "" }).value.trim();
  const category = (document.getElementById("fCategory") || { value: "" }).value.trim() || "Custom";
  const difficulty = (document.getElementById("fDifficulty") || { value: "" }).value.trim() || "Custom";
  const time = (document.getElementById("fTime") || { value: "" }).value.trim() || "Not set";
  const serves = (document.getElementById("fServes") || { value: "" }).value.trim() || "Not set";
  const description = (document.getElementById("fDescription") || { value: "" }).value.trim() || "A user-added recipe.";
  const tagsInput = document.getElementById("fTags");
  const tags = tagsInput ? tagsInput.value.split(",").map(x => x.trim()).filter(Boolean) : [];
  const ingredients = getLines("fIngredients");
  const notes = getLines("fNotes");
  const rawSteps = getLines("fSteps");

  if (!title) {
    alert("Add a recipe name first.");
    return;
  }
  const steps = rawSteps.map((line, i) => {
    const parts = line.split("|").map(x => x.trim());
    return { title: parts[0] || `Step ${i + 1}`, heat: parts[1] || "Medium", time: parts[2] || "", body: parts[3] || parts[0] || "" };
  });
  if (!steps.length) {
    alert("Add at least one step.");
    return;
  }
  const newRecipe = {
    id: slug(title + "-" + Date.now()),
    title, category, difficulty, time, serves, description,
    tags: tags.length ? tags : ["Custom"],
    ingredients: ingredients.length ? ingredients : ["Add ingredients"],
    seasoning: [],
    notes: notes.length ? notes : ["Taste as you go and steady the heat if the pan gets too aggressive."],
    steps,
    plating: [
      "Use a clean plate and wipe the rim before serving.",
      "Keep the food grouped neatly rather than spread out randomly.",
      "Finish with a final garnish or contrast if the dish needs colour."
    ]
  };
  saveCustomRecipe(newRecipe);
  recipes = loadRecipes();
  renderFilters();
  renderRecipes();
  closeAddModal();
  ["fTitle","fCategory","fDifficulty","fTime","fServes","fDescription","fTags","fIngredients","fNotes","fSteps"].forEach(id => {
    const el = document.getElementById(id);
    if (el) el.value = "";
  });
}

