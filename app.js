function getLines(id) {
  return document.getElementById(id).value.split("\n").map(x => x.trim()).filter(Boolean);
}

function saveNewRecipe() {
  const title = document.getElementById("fTitle").value.trim();
  const category = document.getElementById("fCategory").value.trim() || "Custom";
  const difficulty = document.getElementById("fDifficulty").value.trim() || "Custom";
  const time = document.getElementById("fTime").value.trim() || "Not set";
  const serves = document.getElementById("fServes").value.trim() || "Not set";
  const description = document.getElementById("fDescription").value.trim() || "A user-added recipe.";
  const tags = document.getElementById("fTags").value.split(",").map(x => x.trim()).filter(Boolean);
  const ingredients = getLines("fIngredients");
  const notes = getLines("fNotes");
  const rawSteps = getLines("fSteps");

  if (!title) {
    alert("Add a recipe name first.");
    return;
  }

  const steps = rawSteps.map((line, i) => {
    const parts = line.split("|").map(x => x.trim());
    return {
      title: parts[0] || `Step ${i + 1}`,
      heat: parts[1] || "Medium",
      time: parts[2] || "",
      body: parts[3] || parts[0] || ""
    };
  });

  if (!steps.length) {
    alert("Add at least one step.");
    return;
  }

  const newRecipe = {
    id: slug(title + "-" + Date.now()),
    title, category, difficulty, time, serves, description,
    tags: tags.length ? tags : ["Custom"],
    ingredients: ingredients.length ? ingredients : ["Add ingredients"],
    notes: notes.length ? notes : ["Add your own chef notes"],
    steps
  };

  saveCustomRecipe(newRecipe);
  renderFilters();
  renderRecipes();
  closeAddModal();

  ["fTitle","fCategory","fDifficulty","fTime","fServes","fDescription","fTags","fIngredients","fNotes","fSteps"].forEach(id => {
    document.getElementById(id).value = "";
  });
}

