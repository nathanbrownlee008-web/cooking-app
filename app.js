function renderIdeas(ideas, ingredients) {
  if (!ideasList) return;
  ideasList.innerHTML = ideas.map((idea, index) => `
    <article class="idea-card">
      <h3>${idea.title}</h3>
      <p>${idea.reason}</p>
      <div class="idea-tags">
        <span class="idea-chip">${idea.category}</span>
        <span class="idea-chip">${idea.method}</span>
        <span class="idea-chip">${idea.heat}</span>
      </div>
      <button class="primary-btn" data-build-index="${index}">Build this recipe</button>
    </article>
  `).join("");

  ideasList.querySelectorAll("[data-build-index]").forEach(btn => {
    btn.addEventListener("click", () => {
      const chosen = ideas[Number(btn.dataset.buildIndex)];
      const recipe = smartRecipeFromIdea(chosen, ingredients);
      const custom = readStore("chef_deluxe_custom_recipes");
      custom.unshift(recipe);
      writeStore("chef_deluxe_custom_recipes", custom);
      recipes = loadRecipes();
      renderFilters();
      renderRecipes();
      closeIdeasModal();
      closeGeneratorModal();
      ["gIngredients","gStyle","gCategory","gDifficulty","gTime","gServes"].forEach(id => {
        const el = document.getElementById(id);
        if (el) el.value = "";
      });
      openRecipe(recipe.id);
    });
  });
}


function saveNewRecipe() {
  const title = document.getElementById("fTitle").value.trim();
  const category = document.getElementById("fCategory").value.trim() || "Custom";
  const difficulty = document.getElementById("fDifficulty").value.trim() || "Custom";
  const time = document.getElementById("fTime").value.trim() || "Not set";
  const serves = document.getElementById("fServes").value.trim() || "Not set";
  const description = document.getElementById("fDescription").value.trim() || "A user-added recipe.";
  const tags = document.getElementById("fTags").value.split(",").map(x => x.trim()).filter(Boolean);
  const ingredients = getLines("fIngredients");
  const notes = getLines("fNotes");
  const rawSteps = getLines("fSteps");

  if (!title) {
    alert("Add a recipe name first.");
    return;
  }

  const steps = rawSteps.map((line, i) => {
    const parts = line.split("|").map(x => x.trim());
    return {
      title: parts[0] || `Step ${i + 1}`,
      heat: parts[1] || "Medium",
      time: parts[2] || "",
      body: parts[3] || parts[0] || ""
    };
  });

  if (!steps.length) {
    alert("Add at least one step.");
    return;
  }

  const seasoning = generatedSeasoning(category);

  const newRecipe = {
    id: slug(title + "-" + Date.now()),
    title,
    category,
    difficulty,
    time,
    serves,
    description,
    tags: tags.length ? tags : ["Custom"],
    ingredients: ingredients.length ? ingredients : ["Add ingredients"],
    notes: notes.length ? notes : ["Taste as you go and steady the heat if the pan gets too aggressive."],
    steps,
    plating: [
      "Use a clean plate and wipe the rim before serving.",
      "Keep the food grouped neatly rather than spread out randomly.",
      "Finish with a final garnish or contrast if the dish needs colour."
    ],
    seasoning
  };

  saveCustomRecipe(newRecipe);
  recipes = loadRecipes();
  renderFilters();
  renderRecipes();
  closeAddModal();

  ["fTitle","fCategory","fDifficulty","fTime","fServes","fDescription","fTags","fIngredients","fNotes","fSteps"].forEach(id => {
    document.getElementById(id).value = "";
  });
}

